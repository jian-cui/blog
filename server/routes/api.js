function getTags(data) {
  let tagObj = [];
  for (let i = 0; i < data.length; i++) {
    tagObj.push({
      name: data.title,
      url: '/tag/' + data.tag_id
    })
  }
  return tagObj;
}
router.post('/tagList', function (req, res, next) {
  connection.query('SELECT id, title as tagName from tag', function (err, results) {
    if (err) next(err);
    res.send(results);
  })
})

